public abstract class Animal {
    String name;
    int run;
    int swim;


    public Animal() {
    }

    public Animal(String name, int run, int swim) {

        this.name = name;
        this.run = run;
        this.swim = swim;
    }

    public void animalInfo() {
    }

    public void run(int run) {
    }

    public void swim(int swim) {
    }

}
